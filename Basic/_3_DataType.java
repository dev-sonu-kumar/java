package Basic;

public class _3_DataType {
    public static void main(String[] args) {
        int number = 123; // Whole numbers
        double floatNumber = 123.55; // Decimal numbers
        char grade = 'A'; // Single character
        boolean isPersent = true; // True/false values
        String name = "Sonu K"; // Text (class, not a type)

        System.out.println("number is: " + number);
        System.out.println("floatNumber is: " + floatNumber);
        System.out.println("grade is: " + grade);
        System.out.println("isPersent: " + isPersent);
        System.out.println("name: " + name);
    }
}
