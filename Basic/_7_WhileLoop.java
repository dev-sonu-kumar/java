package Basic;

public class _7_WhileLoop {
    public static void main(String[] args) {

        // print 1 to 10

        int i = 1;
        while (i <= 10) {
            System.out.println(i);
            i++;
        }

        System.out.println("................");

        // print 10 to 1

        int j = 10;
        while (j >= 1) {
            System.out.println(j);
            j--;
        }

    }
}
